user_age = int(input("Enter your age : "))
months = user_age * 12
days = user_age * 365
hrs = days * 24
mins = hrs * 60
secs = mins * 60
print(f"Your age, {user_age}, is equal to {months} months.")
print(f"Your age, {user_age}, is equal to {days} days.")
print(f"Your age, {user_age}, is equal to {hrs} hrs.")
print(f"Your age, {user_age}, is equal to {mins} mins.")
print(f"Your age, {user_age}, is equal to {secs} secs.")
